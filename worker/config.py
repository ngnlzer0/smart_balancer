"""
Модуль конфігурації (Configuration Module).
===========================================

Цей клас при ініціалізації зчитує змінні середовища (Environment Variables),
які Docker Compose прокидає з файлу .env. Якщо змінної немає, використовується
безпечне значення за замовчуванням (fallback value).
"""

import os
import logging

# ============================================================================
# Налаштування глобального логера для Control Plane
# ============================================================================
# Використання єдиного логера гарантує консистентність формату логів у всіх
# модулях застосунку. Формат включає час, рівень важливості (INFO, ERROR) та повідомлення.
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s | %(levelname)-8s | %(message)s',
    datefmt='%Y-%m-%d %H:%M:%S'
)
logger = logging.getLogger("ControlPlane")


class Config:
    """
    Головний клас конфігурації.
    """

    # ------------------------------------------------------------------------
    # ІНФРАСТРУКТУРНІ НАЛАШТУВАННЯ (Infrastructure Settings)
    # ------------------------------------------------------------------------

    # Ім'я хоста Redis у внутрішній мережі Docker (DNS resolution)
    REDIS_HOST: str = os.getenv("REDIS_HOST", "redis")

    # Назва спільної мережі Docker, де ми будемо шукати IP-адреси бекендів
    # (Service Discovery)
    NETWORK_NAME: str = os.getenv("NETWORK_NAME", "course_work_network")

    # Порт, на якому працює наш FastAPI застосунок всередині контейнерів
    BACKEND_PORT: int = int(os.getenv("BACKEND_PORT", 8000))

    # Частота роботи головного циклу балансувальника (Tick Rate).
    # 1.0 означає, що метрики збираються та перераховуються кожну 1 секунду.
    POLL_INTERVAL: float = float(os.getenv("POLL_INTERVAL", 1.0))

    # ------------------------------------------------------------------------
    # НАЛАШТУВАННЯ АЛГОРИТМУ БАЛАНСУВАННЯ (Math Model Parameters)
    # ------------------------------------------------------------------------
    # Зберігаємо їх у словнику, щоб головний скрипт міг їх скопіювати і
    # динамічно оновлювати з Redis під час роботи системи.
    DEFAULT_MATH_CONFIG = {

        # --- Вагові коефіцієнти багатокритеріальної оцінки (MCDM) ---
        # Сума цих 4 параметрів в ідеалі має дорівнювати 1.0 (100%).

        # Наскільки алгоритм зважає на завантаження процесора (CPU)
        'ALPHA': float(os.getenv("ALPHA", 0.35)),

        # Наскільки алгоритм зважає на використання оперативної пам'яті (RAM)
        'BETA': float(os.getenv("BETA", 0.05)),

        # Наскільки алгоритм зважає на час відгуку бекенда (Latency).
        # Зазвичай це найвищий пріоритет, бо він напряму впливає на користувачів.
        'GAMMA': float(os.getenv("GAMMA", 0.40)),

        # Наскільки алгоритм зважає на кількість активних з'єднань/потоків
        'DELTA': float(os.getenv("DELTA", 0.20)),

        # --- Порогові значення для нормалізації ---

        # Максимально допустимий час відгуку в мілісекундах (SLA Limit).
        # Якщо сервер відповідає 1000мс або довше, його метрика 'r' стає 1.0 (100% завантаження).
        'T_MAX': float(os.getenv("T_MAX", 1000.0)),

        # Ліміт одночасних з'єднань на один сервер.
        'A_MAX': float(os.getenv("A_MAX", 100.0)),

        # --- Параметри адаптивності (The Secret Sauce) ---

        # Штрафний мультиплікатор за помилки. Якщо сервер повертає 5xx помилки,
        # його навантаження множиться на (1 + PENALTY * error_rate).
        'PENALTY': float(os.getenv("PENALTY", 10.0)),

        # Коефіцієнт експоненційного згладжування (EWMA).
        # Визначає чутливість до різких стрибків трафіку (захищає від осциляцій).
        'RHO': float(os.getenv("RHO", 0.3)),

        # --- Налаштування відмовостійкості (Fault Tolerance) ---

        # Circuit Breaker Threshold: скільки разів поспіль сервер може не відповісти,
        # перш ніж ми перестанемо його опитувати і марнувати ресурси мережі.
        'CB_THRESHOLD': int(os.getenv("CB_THRESHOLD", 3))
    }


# Створюємо глобальний екземпляр конфігу, який будуть імпортувати інші модулі
config = Config()